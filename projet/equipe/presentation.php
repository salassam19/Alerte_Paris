	<p>
		Les risques liés aux catastrophes naturelles augmentent et constituent une menace croissante pour les pays, leur économie et leur population.
	</p>
	<p>
		Des investissements appropriés face aux dangers naturels peuvent éviter que ceux-ci ne se transforment en catastrophe humaine, mais il faut pour cela disposer de données précises et fiables sur ces risques, particulièrement ceux de Paris, et pouvoir les communiquer au public.
	</p>
	<p>
		Quand une catastrophe naturelle se produit, il y a des personnes qui souhaitent proposer leur aide, mais ne savent pas à qui proposer cette aide lorsque la catastrophe se produit. Grâce au site  « Alerte Paris », les personnes ayant des problème suite à une inondation, une tempête, une canicule, etc., pourraient se mettre en relation directement avec les personnes sinistrées. Ce site qui s’active en cas de catastrophe naturelle ou industrielle, permet de poster une demande d’aide, de trouver les abris privés et publics les plus proches, grâce à son application maps.
	</p>
