	<h3>« Alerte Paris, le site solidaire en cas de catastrophe »</h3>
	<p>
		Les risques liés aux catastrophes naturelles augmentent et constituent une menace croissante pour les pays, leur économie et leur population.
	</p>
	<p>
		Des investissements appropriés face aux dangers naturels peuvent éviter que ceux-ci ne se transforment en catastrophe humaine, mais il faut pour cela disposer de données précises et fiables sur ces risques, particulièrement ceux de Paris, et pouvoir les communiquer au public.
	</p>
	<p>
		Quand une catastrophe naturelle se produise, il y a des personnes que voulaient  proposer son aide, mais ne savaient pas à qui proposer cette aide le jour de la catastrophe. Grâce à ce site  « Alerte Paris » les personnes qui ont de problèmes à cause des dégâts d’inondation, tempêtes, canicule, etc., pourraient se mettre en relation facilement avec les personnes qui voulaient les aides. Ce site qui s’active en cas de catastrophe naturelle ou industrielle, permet de poster de demande d’aide, de trouver les abris les plus proche grâce à son application maps, et des personnes que pourront les héberger. 
	</p>
